    private void Bt_EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Bt_EliminarActionPerformed
        // TODO add your handling code here:
        NuevoClienteController cliente = new NuevoClienteController();
        if(idClientes == 0){
            JOptionPane.showMessageDialog(null, "Seleccione un cliente!");
            
        }else{
            if(cliente.eliminar(idClientes)){
               JOptionPane.showMessageDialog(null, "Cliente Eliminado!");
               this.CargarTablaClientes();
               this.Limpiar();
            }else{
               JOptionPane.showMessageDialog(null, "Seleccione un cliente!");
            }
        }
    }//GEN-LAST:event_Bt_EliminarActionPerformed

    private void Bt_ActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Bt_ActualizarActionPerformed
        // TODO add your handling code here:
        if(!txt_CUI.getText().isEmpty() || !txt_NOMBRE.getText().isEmpty() || !txt_APELLIDO.getText().isEmpty() || !txt_TELEFONO.getText().isEmpty() || !txt_DIRECCION.getText().isEmpty()){
            NuevoClienteModel clienteModel = new NuevoClienteModel();
            NuevoClienteController clienteController = new NuevoClienteController();
            
            if(clienteController.actualizarCliente(clienteModel, idClientes)){
                JOptionPane.showMessageDialog(null, "Registro Actualizado!");
                this.CargarTablaClientes();
                this.Limpiar();
            }else{
                 JOptionPane.showMessageDialog(null, "Error al actualizar!");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Seleccione un cliente!");
        }
    }//GEN-LAST:event_Bt_ActualizarActionPerformed


