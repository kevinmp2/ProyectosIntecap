    private void enviarDatosClienteSeleccionado(int idCliente){
        try {
            Connection conec = Conexion.conectarDB();
            PreparedStatement pst = conec.prepareStatement("SELECT * FROM Cliente WHERE ID_Cliente = '"+ idCliente + "'");
            ResultSet rs = pst.executeQuery();
            
            if(rs.next()) {
                txt_CUI.setText(rs.getString("CUI"));
                txt_NOMBRE.setText(rs.getString("NOMBRE"));
                txt_APELLIDO.setText(rs.getString("APELLIDO"));
                txt_TELEFONO.setText(rs.getString("TELEFONO"));
                txt_DIRECCION.setText(rs.getString("DIRECCION"));
            }
            
        } catch (Exception e) {
        }
    }
    

    private void CargarTablaClientes(){
        Connection conec = Conexion.conectarDB();
        DefaultTableModel tableModel = new DefaultTableModel();
        String sql = "SELECT ID_Cliente, CUI, NOMBRE, APELLIDO, TELEFONO, DIRECCION FROM Cliente";
        Statement st;
        
        try {
            st = conec.createStatement();
            ResultSet rs = st.executeQuery(sql);
            GestionarCliente.TablaClientes = new JTable(tableModel);
            GestionarCliente.jScrollPane1.setViewportView(GestionarCliente.TablaClientes);
            
            tableModel.addColumn("ID");
            tableModel.addColumn("CUI");
            tableModel.addColumn("NOMBRE");
            tableModel.addColumn("APELLIDO");
            tableModel.addColumn("TELEFONO");
            tableModel.addColumn("DIRECCION");
            
            while(rs.next()){
                Object fila[] = new Object[6];
                
                for(int i=0; i < 6; i++){
                    fila[i] = rs.getObject(i + 1);
                }
                tableModel.addRow(fila);
                
            }
            
            conec.close();
            
        } catch (SQLException e) {
            System.out.println("Error al llenar la tabla clientes: " + e);
        }
        
        
        TablaClientes.addMouseListener(new MouseAdapter(){
            @Override
            public void mouseClicked(MouseEvent e){
                int fila_point = TablaClientes.rowAtPoint(e.getPoint());
                int columna_point = 0;
                
                if(fila_point > -1 ) {
                    idClientes = (int) tableModel.getValueAt(fila_point, columna_point);
                    enviarDatosClienteSeleccionado(idClientes);
                } 
                
            }
        });
    }
    
    private void Limpiar(){
        txt_CUI.setText("");
        txt_NOMBRE.setText("");
        txt_APELLIDO.setText("");
        txt_TELEFONO.setText("");
        txt_DIRECCION.setText("");
    }

